package com.example.evanta;

public class User {
        private String uid;
        private String name;
        private String email;
        private String whatsappno;

        public User(String uid, String name, String email, String whatsappno) {
            this.uid = uid;
            this.name = name;
            this.email = email;
            this.whatsappno = whatsappno;
        }
}
